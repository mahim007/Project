    private void jTextField4KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField4KeyReleased
        // TODO add your handling code here:
        try{
        Session session = Connector.getSession();
        session.beginTransaction();

        // insert
        if(!jTextField4.getText().isEmpty()){
        Student student = (Student) session.load(Student.class, Long.parseLong(jTextField4.getText()));
         jTextField1.setText(student.getName());
         jTextField2.setText(student.getfName());
         jTextField3.setText(student.getRoll());
        
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Failed to search."+e.getMessage());
        }
        
    }//GEN-LAST:event_jTextField4KeyReleased

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try{
        Session session = Connector.getSession();
        session.beginTransaction();

        // insert
        if(!jTextField4.getText().isEmpty()){
         Student student = (Student) session.load(Student.class, Long.parseLong(jTextField4.getText()));
         session.delete(student);
        session.getTransaction().commit();
        
        jTextField1.setText("");
         jTextField2.setText("");
         jTextField3.setText("");
          JOptionPane.showMessageDialog(rootPane, "'"+student.getName()+"' has deleted successfully.");
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Failed to delete."+e.getMessage());
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

