    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
        Session session = Connector.getSession();
        session.beginTransaction();

        // insert
        Student student = new Student();
        student.setName(jTextField1.getText());
        student.setfName(jTextField2.getText());
        student.setRoll(jTextField3.getText());
        session.save(student);

        session.getTransaction().commit();
        JOptionPane.showMessageDialog(rootPane, "Student saved successfully.");
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Failed to save."+e.getMessage());
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

